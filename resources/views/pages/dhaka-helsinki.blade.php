                    <p style="margin:auto"><strong> Finnish president expressed his concern that Bangladesh will be badly affected if the sea level rises.</strong><br>

                      Bangladesh and Finland have agreed to work together on the issue of
                      climate change at a meeting between Prime Minister Sheikh Hasina and Finland’s
                      President Sauli Niinisto.The prime minister’s Speech Writer, Secretary Md
                      Nazrul Islam, briefed reporters after the meeting, held at the president’s
                      official residence in Helsinki, on Tuesday, reports BSS.<br>
                      
                      The prime minister said one third of Bangladesh will be submerged if the
                      sea level rises one meter.<br>
                      
                      She continued by highlighting her government’s initiatives to mitigate
                      the consequences of climate change; such as theformation of Bangladesh Climate
                      Resilience Fund with its own resources to face the impacts of climate change.<br>
                      
                      Sheikh Hasina informed the president of the measures her government is taking; including
                      creating a green belt along the coast of the Bay of Bengal and organizing
                      volunteers to offset the impact of natural disasters like cyclones and tidal
                      surges.<br>
                      
                      The Finnish president expressed his concern that Bangladesh will be badly
                      affected if the sea level rises due to climate change.<br>
                      
                      He emphasized the importance of strengthening global warning systems to
                      reduce losses from natural calamities – especially tidal surges.<br><br>
                      
                      <strong>Rohingya issue</strong><br>
                      
                      At the meeting, the prime minister also sought support from the European
                      Union for the safe and dignified repatriation of the 1,100,000 Rohingya people
                      from Bangladesh.<br>
                      
                      Sheikh Hasina said that Bangladesh is a densely populated country and it
                      is very tough to give shelter to the vast number of Rohingya people.<br>
                      
                      The prime minister expressed her concern that Myanmar has not kept its
                      promise of taking back the Rohingya people even after signing agreements to
                      this end.<br>
                      
                      Responding to a question from the Finnish president, she said relations
                      with Myanmar have not deteriorated even after the forceful eviction of 1.1m
                      Rohingya people by the Myanmar authorities.<br>
                      
                      She said that Bangladesh pursues a foreign policy of friendship with all,
                      malice to none – and Bangladesh maintains very good relations with its
                      neighbours.<br>
                      
                      The premier overviewed different development indices—including 8.13% GDP
                      growth this year—and urged investors from Finland to invest in Bangladesh.<br>
                      
                      Noting that her government is setting up 100 special economic zones,
                      Sheikh Hasina explained that Finnish investors could have a special economic
                      zone if they would like.
                      She lauded Finland for recognizing Bangladesh immediately after the
                      Liberation War in 1971.<br>
                      
                      “We always value the support and cooperation that Finland extended to
                      Bangladesh during and after the Liberation War of the country,” said the
                      premier.<br>
                      
                      The prime minister narrated how Father of the Nation Bangabandhu Sheikh
                      Mujibur Rahman struggled his whole life and was subjected to incarceration for
                      the emancipation of people and independence of the country.<br>
                      
                      Responding to a query of the president, Sheikh Hasina recollected her own
                      struggle after the assassination of the Father of the Nation along with most of
                      his family members.<br>
                      
                      She said she returned home with the people’s support in 1981 and it was
                       the people who voted her in four times as the prime minister of Bangladesh.
                      The Finnish president praised Sheikh Hasina for the economic uplift of
                      Bangladesh under her leadership.<br>
                      
                      The prime minister then invited the Finnish president to visit Bangladesh
                      and sought Finland’s support in electing Bangladesh Foreign Secretary Shahidul
                      Haque as the Deputy Director General of IOM.<br>
                      
                      At the beginning of the meeting, both the prime minister and the
                      president exchanged greetings.<br>
                      
                      The president received Sheikh Hasina at his residence on her arrival.
                      The prime minister signed the visitors’ book, said Secretary Md Nazrul
                      Islam. .<br>
                      
                      Sheikh Hasina arrived in Finland on Monday afternoon for a five-day
                      official visit.</p>
